def format_products_for_llm() -> str:
    lines = ["CATALOGUE PRODUITS MWEMA BEAUTY:\n"]
    for p in PRODUCTS:
        lines.append(f"- {p['name']} ({p['category']}) - {p['price']}$ CAD")
        lines.append(f"  {p['description']}")
        lines.append(f"  Ideal pour: {p['ideal_for']}\n")
    return "\n".join(lines)

def format_services_for_llm() -> str:
    lines = ["SERVICES MWEMA BEAUTY:\n"]
    current_cat = None
    for s in SERVICES:
        if s["category"] != current_cat:
            current_cat = s["category"]
            lines.append(f"\n[{current_cat.upper()}]")
        lines.append(f"- {s['name']} - {s['price']}$ ({s['duration_hours']}h)")
        lines.append(f"  {s['description']}")
    return "\n".join(lines)

